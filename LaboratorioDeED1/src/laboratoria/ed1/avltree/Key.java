package laboratoria.ed1.avltree;

public class Key implements Comparable<Key>{
	
	private String val;
	
	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}

	@Override
	public int compareTo(Key o) {
		return this.compareTo(o);		
	}

}
